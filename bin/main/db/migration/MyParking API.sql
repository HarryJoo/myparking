DROP TABLE IF EXISTS parking_lots;

CREATE TABLE  parking_lots  (
	 id	bigint	NOT NULL  AUTO_INCREMENT PRIMARY KEY ,
	 parking_lot_no 	varchar(255)	NOT NULL,
	 parking_lot_nm 	text	NOT NULL,
	 parking_lot_section 	varchar(255)	NOT NULL,
	 parking_lot_type 	varchar(255)	NOT NULL,
	 road_name_address 	text	NULL,
	 number_name_address 	text	NOT NULL,
	 parking_compartment 	bigint	NOT NULL,
	 feed_division 	varchar(255)	NOT NULL,
	 alternate_day_no_driving_division 	varchar(255)	NOT NULL,
	 operating_day 	varchar(255)	NOT NULL,
	 weekday_opening_hour 	time	NOT NULL,
	 weekday_closing_hour 	time	NOT NULL,
	 saturday_opening_hour 	time	NOT NULL,
	 saturday_closing_hour 	time	NOT NULL,
	 holiday_opening_hour 	time	NOT NULL,
	 holiday_closing_hour 	time	NOT NULL,
	 parking_fee_infomation 	varchar(255)	NOT NULL,
	 basic_parking_time 	bigint	NOT NULL,
	 basic_parking_fee 	bigint	NOT NULL,
	 additional_unit_time 	varchar(255)	NOT NULL	DEFAULT 0,
	 additional_parking_fee 	varchar(255)	NOT NULL	DEFAULT 0,
	 day_parking_fee_application_time 	varchar(255)	NOT NULL	DEFAULT 0,
	 day_parking_fee 	varchar(255)	NOT NULL	DEFAULT 0,
	 monthly_fee 	varchar(255)	NOT NULL	DEFAULT 0,
	 payment_method 	varchar(255)	NULL,
	 remarks 	varchar(255)	NULL,
	 management_agency_name 	text	NOT NULL,
	 management_agency_phone_number 	varchar(255)	NOT NULL,
	 latitude 	varchar(255)	NOT NULL,
	 longitude 	varchar(255)	NOT NULL,
	 reference_date 	datetime	NOT NULL,
	 institude_code 	varchar(255)	NOT NULL,
	 institude_name 	text	NOT NULL
);
